package br.com.util;

/**
 *
 * @author santos
 */
public class TrataCampos {

	private String coluna;
	private String isentity;

	public TrataCampos(String coluna, String isentity) {

		this.coluna = coluna;
		this.isentity = isentity;

	}

	public String getColuna() {

		return coluna;

	}

	public String getisEntity() {

		return isentity;

	}

	public void setColuna(String coluna) {

		this.coluna = coluna;
	}

	public void isEntity(String isentity) {

		this.isentity = isentity;
	}

}
